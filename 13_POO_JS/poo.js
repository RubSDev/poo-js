/*

Polymorphism
¿Qué es Polimorfismo?
Trabaja cuando hay herencia. Es la capacidad que tienen los objetos de una clase de responder a un evento en función de los parámetros utilizados en su invocación, en otras palabras, invoca un método heredado y lo responde de diferente manera. 


*/

/*

¿Qué es Sobrecarga?
También Overloading. Es cuando dos o más métodos tienen el mismo nombre, pero diferente funcionalidad dependiendo de sus parámetros.
*mismo nombre, diferentes parámetros*

*/
/*
¿Qué es Sobreescritura? 
También Overriding. La sobreescritura de métodos nos permite redefinir un método que heredamos para que este funcione con el mismo nombre y de acuerdo a nuestras necesidades y no a lo definido en la superclase.
Es decir, si tengo una clase padre con el método ingresar() yo puedo crear en la clase hija un método que también se llame ingresar() pero implementándolo según lo que necesite.
*/
