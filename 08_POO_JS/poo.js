/*

Dos definicones aceptadas para definir la POO (programación orientada a objetos)
-Capacidad de modelar problemas a través de objetos
-Soporte de algunos principios que garanticen la modularidad y la reusabilidad del código

*/
/*

MODELOS A TRAVÉZ DE OBJETOS
-Asociación
Objetos con la capacidad de referir otros objetos
-Agregación
La capacidad de referir a otros objetos independientes
-Composición
Objetos con la capacidad de referir otros objetos dependientes

*/

/*
PRINCIPIOS ORIENTADOS A OBJETOS

Encapsulación
Es el ocultamiento o almacenamiento de los elementos de una clase (métodos y atributos) para evitar cambios imprevistos.
Público. Todos acceden a los datos.
Protegido. Accesible dentro de clases y subclases.
Privado. Solo la misma clase puede acceder a estos datos.


Herencia 
Herencia Jerárquica: una clase sirve como una superclase (clase base) para más de una subclase.
Herencia Múltiple (a través de interfaces): una clase puede tener más de una superclase y heredar características de todas las clases principales


Polimorfismo
Trabaja cuando hay herencia. Es la capacidad que tienen los objetos de una clase de responder a un evento en función de los parámetros utilizados en su invocación, en otras palabras, invoca un método heredado y lo responde de diferente manera. 


*/
